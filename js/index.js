function exibirTemas() {
  let listaTemas = document.getElementById('escolha-temas')

  for (let tema in temas) {
    listaTemas.innerHTML += `
      <li class="tema">
        <a href="jogo.html">
          <button type="button" class="temaButton">
            ${tema}
          </button>
          </a>
      </li>
    `
  }
}

function temaEscolhido() {
  let botoesTema = [...document.querySelectorAll('li.tema button')]

  botoesTema.forEach(botao => {
    botao.addEventListener('click', () => {
      localStorage.setItem('temaEscolhido', botao.textContent.trim())
    })
  })
}

function dificuldadeEscolhida() {
  let dificuldadeSelect = document.getElementById('dificuldade-select')

  dificuldadeSelect.addEventListener('change', function() {
    localStorage.setItem('dificuldade', dificuldadeSelect.value)
  })
}

document.addEventListener('DOMContentLoaded', () => {
  dificuldadeEscolhida()
  exibirTemas()
  temaEscolhido()
});
